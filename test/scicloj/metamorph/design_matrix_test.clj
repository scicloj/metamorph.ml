(defn- lm [data target-specs feature-specs]
  (let [dm
        (dm/create-design-matrix data target-specs feature-specs)]
    (regression/lm
     (->
      (cf/target dm)
      (ds/columns)
      first
      vec)
     (->
      (cf/feature dm)
      (ds/rowvecs))
     {:intercept? true
      :names (vec 
              
              (ds/column-names (cf/feature dm))
              )})))
  
