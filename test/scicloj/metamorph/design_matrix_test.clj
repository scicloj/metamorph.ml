(deftest all-col-names-varints
  (let [ds
        (tc/dataset
         {:a [0 1 2]
          'a [2 3 4]
          "a" [4 5 6]
          :y [0 0 0]})
        dm
        (dm/create-design-matrix
         ds
         [:y]
         [[:sum '(clojure.core/+ :a a "a")]])]

    (is (= [6 9 12]
           (:sum dm)))))

